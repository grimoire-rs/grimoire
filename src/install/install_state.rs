    /// A `ClientOutput` pointing at a managed entry inside `cfg`, hashed
    /// over `value`'s canonical serialization, for an arbitrary `client`
    /// name / config filename (so a TOML-spliced client can share this
    /// helper with the JSON-spliced ones below).
    fn entry_output_for(
        tmp: &std::path::Path,
        client: &str,
        filename: &str,
        pointer: &str,
        value: &serde_json::Value,
    ) -> (ClientOutput, AnchorRoots) {
        let roots = AnchorRoots {
            workspace: tmp.to_path_buf(),
            grim_home: tmp.join("grim-home"),
            claude_root: None,
            copilot_root: None,
            opencode_skills: None,
            claude_user_dir: None,
            agents_skills: None,
            codex_root: None,
            cursor_root: None,
            kiro_root: None,
            junie_root: None,
            gemini_root: None,
            zed_root: None,
            amp_root: None,
        };
        let out = ClientOutput {
            client: client.to_string(),
            target: AnchoredPath {
                anchor: PathAnchor::Workspace,
                relative: filename.to_string(),
            },
            content_hash: entry_value_hash(value).unwrap(),
            support_dir: None,
            entry: Some(pointer.to_string()),
        };
        (out, roots)
    }

    /// A Claude (JSON-spliced) `ClientOutput` pointing at a managed entry
    /// inside `.mcp.json`, hashed over `value`'s canonical serialization.
    fn entry_output(tmp: &std::path::Path, pointer: &str, value: &serde_json::Value) -> (ClientOutput, AnchorRoots) {
        entry_output_for(tmp, "claude", ".mcp.json", pointer, value)
    }

    #[test]
    fn entry_hash_ignores_formatting_and_key_order() {
        let tmp = tempfile::tempdir().unwrap();
        let value = serde_json::json!({"command": "grim", "args": ["mcp"]});
        let (out, roots) = entry_output(tmp.path(), "/mcpServers/grim", &value);

        // Reordered keys, exotic whitespace, extra foreign keys: same hash.
        std::fs::write(
            tmp.path().join(".mcp.json"),
            "{\n  \"other\": 1,\n  \"mcpServers\": {\n    \"grim\": {\n\t\"args\":   [\"mcp\"],\n      \"command\": \"grim\"\n    }\n  }\n}\n",
        )
        .unwrap();
        assert!(out.is_present(&roots, Containment::Strict).unwrap());
        assert_eq!(out.current_hash(&roots, Containment::Strict).unwrap(), out.content_hash);

        // A real value change flips the hash.
        std::fs::write(
            tmp.path().join(".mcp.json"),
            "{\"mcpServers\": {\"grim\": {\"command\": \"evil\", \"args\": [\"mcp\"]}}}",
        )
        .unwrap();
        assert!(out.is_present(&roots, Containment::Strict).unwrap());
        assert_ne!(out.current_hash(&roots, Containment::Strict).unwrap(), out.content_hash);
    }

    #[test]
    fn entry_presence_tracks_the_member_not_the_file() {
        let tmp = tempfile::tempdir().unwrap();
        let value = serde_json::json!({"command": "grim"});
        let (out, roots) = entry_output(tmp.path(), "/mcpServers/grim", &value);

        // No file at all.
        assert!(!out.is_present(&roots, Containment::Strict).unwrap());
        // File present, member absent.
        std::fs::write(tmp.path().join(".mcp.json"), "{\"mcpServers\": {\"other\": {}}}").unwrap();
        assert!(!out.is_present(&roots, Containment::Strict).unwrap());
        assert!(
            out.current_hash(&roots, Containment::Strict).is_err(),
            "absent entry has no current hash"
        );
        // Unparseable file: absent, not an error (read-side degradation).
        std::fs::write(tmp.path().join(".mcp.json"), "not json {{{").unwrap();
        assert!(!out.is_present(&roots, Containment::Strict).unwrap());
        // Member present.
        std::fs::write(
            tmp.path().join(".mcp.json"),
            "{\"mcpServers\": {\"grim\": {\"command\": \"grim\"}}}",
        )
        .unwrap();
        assert!(out.is_present(&roots, Containment::Strict).unwrap());
    }

