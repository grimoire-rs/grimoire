//! The client-transform seam: turn a materialized canonical artifact tree
//! into the on-disk layout one client expects.
//!
//! **Skills**: every client copies the tree verbatim, except `SKILL.md`
//! when it carries tool-namespaced metadata (`claude.<field>: "…"`): then
//! the index is projected per client via [`super::render`] — Claude gets
//! the native typed fields lifted to the top level; OpenCode/Copilot get
//! a clean universal frontmatter with the tool keys removed.
//!
//! **Rules**: Claude copies the index verbatim (`paths:` is native).
//! OpenCode rewrites it to provenance + body — OpenCode has no per-file
//! path scoping; the file is loaded via the managed `instructions` entry
//! in `opencode.json` (see `opencode_config`). Copilot rewrites it to a
//! `<name>.instructions.md` whose frontmatter maps `paths` → `applyTo`
//! (comma-joined) and `copilot.exclude-agent` → `excludeAgent`.
//!
//! Every transform is deterministic — regenerating the same artifact at
//! the same pinned identifier yields byte-identical output, so a user
//! edit to a generated file is detected as drift exactly like a canonical
//! file (the installer hashes the *expected* generated bytes, not the
//! input).

