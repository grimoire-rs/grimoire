    #[test]
    fn materialize_skill_rebound_binding_rewrites_frontmatter_name() {
        // Regression: a `--name` rebinding (binding ≠ frontmatter name)
        // must keep the Agent Skills directory-equality rule true at the
        // destination — the installed SKILL.md carries the binding as its
        // `name`, is marked generated, and every other key plus the body
        // survives. Siblings stay verbatim.
        let tmp = tempfile::tempdir().unwrap();
        let root = tmp.path().join("foo");
        std::fs::create_dir_all(root.join("scripts")).unwrap();
        std::fs::write(
            root.join("SKILL.md"),
            "---\nname: foo\ndescription: d\nmetadata:\n  keywords: a,b\n---\n# body\n",
        )
        .unwrap();
        std::fs::write(root.join("scripts/run.sh"), "echo hi\n").unwrap();

        for client in [ClientTarget::Claude, ClientTarget::OpenCode, ClientTarget::Copilot] {
            let dest = tmp.path().join(format!("out-{client}/bar"));
            let files = client
                .materialize(MaterializeRequest {
                    kind: ArtifactKind::Skill,
                    name: "bar",
                    artifact_root: &root,
                    dest: &dest,
                    scope: ConfigScope::Project,
                    pinned: "p",
                    support_dir: None,
                })
                .unwrap();
            let index = dest.join("SKILL.md");
            assert!(
                files.iter().any(|f| f.path == index && f.generated),
                "{client}: rebound SKILL.md must be generated"
            );
            let doc = std::fs::read_to_string(&index).unwrap();
            assert!(doc.contains("name: bar"), "{client}: name rewritten to binding: {doc}");
            assert!(!doc.contains("name: foo"), "{client}: stale name gone: {doc}");
            assert!(doc.contains("keywords: a,b"), "{client}: plain metadata kept: {doc}");
            assert!(doc.ends_with("# body\n"), "{client}: body preserved: {doc}");
            assert_eq!(
                std::fs::read_to_string(dest.join("scripts/run.sh")).unwrap(),
                "echo hi\n",
                "{client}: siblings stay verbatim"
            );
        }
    }

    #[test]
    fn materialize_rule_with_support_dir_copilot_transforms_index_copies_support() {
        // Copilot transforms only the index; the support dir is copied
        // verbatim beside it at `.github/instructions/<name>/`.
        let tmp = tempfile::tempdir().unwrap();
        let src = tmp.path().join("my-rule.md");
        std::fs::write(&src, "---\npaths: [\"a\"]\n---\n# index\nbody\n").unwrap();
        let support = tmp.path().join("my-rule");
        std::fs::create_dir_all(&support).unwrap();
        std::fs::write(support.join("examples.md"), "# ex\n").unwrap();

        let dest = tmp.path().join(".github/instructions/my-rule.instructions.md");
        let files = ClientTarget::Copilot
            .materialize(MaterializeRequest {
                kind: ArtifactKind::Rule,
                name: "my-rule",
                artifact_root: &src,
                dest: &dest,
                scope: ConfigScope::Project,
                pinned: "pin",
                support_dir: Some(support.as_path()),
            })
            .unwrap();

        // The index is the single generated file; support files are verbatim.
        assert_eq!(files.iter().filter(|f| f.generated).count(), 1);
        let index = std::fs::read_to_string(&dest).unwrap();
        assert!(index.starts_with("---\napplyTo: \"a\"\n---\n<!-- generated by grim from pin;"));
        assert!(!index.contains("paths:"), "canonical frontmatter mapped, not copied");
        let support_dest = dest.parent().unwrap().join("my-rule");
        assert_eq!(
            std::fs::read_to_string(support_dest.join("examples.md")).unwrap(),
            "# ex\n"
        );
    }
