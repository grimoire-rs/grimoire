/// One file produced for an client target.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct MaterializedFile {
    /// The absolute on-disk path written.
    pub path: PathBuf,
    /// Whether the file is a generated transform (vs. a canonical copy).
    /// Generated files are integrity-hashed against their *expected*
    /// bytes so a user edit is still detected as drift.
    pub generated: bool,
}

/// The inputs to [`ClientTarget::materialize`]: what to materialize, from
/// where, and to where. Bundled into one struct so the entry point stays a
/// two-argument call and each field is named at the call site.
#[derive(Debug, Clone, Copy)]
pub struct MaterializeRequest<'a> {
    /// The artifact kind (skill / rule / agent).
    pub kind: ArtifactKind,
    /// The binding name (may differ from the canonical frontmatter name).
    pub name: &'a str,
    /// The extracted canonical content root: the skill directory
    /// (`<name>/…`) or the rule / agent index file (`<name>.md`).
    pub artifact_root: &'a Path,
    /// The final on-disk destination from [`ClientTarget::path_for`].
    pub dest: &'a Path,
    /// The install scope.
    pub scope: ConfigScope,
    /// The pinned identifier stamped into generated-file provenance.
    pub pinned: &'a str,
    /// The optional sibling support directory of a multi-file rule.
    pub support_dir: Option<&'a Path>,
}

impl ClientTarget {
    /// Every supported client, in canonical order.
    pub const ALL: [ClientTarget; 10] = [
        Self::Claude,
        Self::OpenCode,
        Self::Copilot,
        Self::Codex,
        Self::Cursor,
        Self::Kiro,
        Self::Junie,
        Self::Gemini,
        Self::Zed,
        Self::Amp,
    ];

    /// The per-vendor materialization strategy behind this identity.
    pub fn vendor(self) -> &'static dyn Vendor {
        match self {
            Self::Claude => &ClaudeVendor,
            Self::OpenCode => &OpenCodeVendor,
            Self::Copilot => &CopilotVendor,
            Self::Codex => &CodexVendor,
            Self::Cursor => &CursorVendor,
            Self::Kiro => &KiroVendor,
            Self::Junie => &JunieVendor,
            Self::Gemini => &GeminiVendor,
            Self::Zed => &ZedVendor,
            Self::Amp => &AmpVendor,
        }
    }

    /// The absolute install path for the named artifact of `kind` under
    /// `workspace` at `scope`. A skill installs as a directory; a rule as
    /// a single file. Project scope lands under `<workspace>/<root_dir>`;
    /// global scope lands in the vendor's native user-level discovery
    /// location (see [`Vendor::skills_root`] / [`Vendor::rule_path`]).
    pub fn path_for(
        self,
        workspace: &Path,
        scope: crate::config::scope::ConfigScope,
        kind: ArtifactKind,
        name: &str,
    ) -> PathBuf {
        match kind {
            ArtifactKind::Skill => self.vendor().skills_root(workspace, scope).join(name),
            ArtifactKind::Rule => self.vendor().rule_path(workspace, scope, name),
            ArtifactKind::Agent => self.vendor().agent_path(workspace, scope, name),
            // An MCP descriptor registers into the vendor's MCP config file
            // (no file of its own); the config path is the reportable
            // target. The workspace-conventional `.mcp.json` stands in when
            // the vendor has no surface for this scope (report-only — the
            // install itself skips such a vendor).
            ArtifactKind::Mcp => self
                .vendor()
                .mcp_config_path(workspace, scope)
                .unwrap_or_else(|| workspace.join(".mcp.json")),
            // Bundles never materialize; they expand into members.
            ArtifactKind::Bundle => unreachable!("bundles are never materialized; they expand into members"),
        }
    }

    /// Materialize the canonical artifact tree at `artifact_root` into the
    /// client layout at `dest`.
    ///
    /// `artifact_root` is the extracted canonical content: the skill
    /// directory (`<name>/…`) or the rule index file (`<name>.md`). `dest`
    /// is the final path from [`Self::path_for`]. For a skill, the tree is
    /// copied verbatim. For a rule, Claude/OpenCode copy the index verbatim;
    /// Copilot transforms it (strip `paths:`, prepend provenance header).
    ///
    /// `support_dir` is the optional sibling support directory of a
    /// multi-file rule (`<root>/<name>/`). When present it is copied
    /// verbatim — for every client, including Copilot — beside the index at
    /// `<dest_parent>/<name>/`, so the index's relative links resolve. Only
    /// the index is ever transformed.
    ///
    /// # Errors
    ///
    /// [`InstallErrorKind::TargetIo`] for a filesystem failure;
    /// [`InstallErrorKind::MaterializeFailed`] for an unreadable rule.
    pub fn materialize(self, req: MaterializeRequest<'_>) -> Result<Vec<MaterializedFile>, InstallError> {
        let MaterializeRequest {
            kind,
            name,
            artifact_root,
            dest,
            scope,
            pinned,
            support_dir,
        } = req;
        match kind {
            ArtifactKind::Skill => self.materialize_skill(name, artifact_root, dest),
            ArtifactKind::Rule => self.materialize_rule(name, artifact_root, dest, scope, pinned, support_dir),
            ArtifactKind::Agent => self.materialize_agent(artifact_root, dest, pinned),
            // Bundles never materialize; they expand into members.
            ArtifactKind::Bundle => unreachable!("bundles are never materialized; they expand into members"),
            // MCP descriptors register into client configs; the installer
            // diverges before the materialization path.
            ArtifactKind::Mcp => unreachable!("mcp descriptors register into client configs, never materialize"),
        }
    }

    /// Copy a skill tree into `dest`. Every file copies verbatim except
    /// `SKILL.md` when it carries tool-namespaced metadata: that index is
    /// rendered per client (and marked `generated`, so the integrity hash
    /// anchors on the *expected rendered* bytes). A `--name` rebinding
    /// (binding `name` ≠ frontmatter name) rewrites the frontmatter
    /// `name` to the binding first — the Agent Skills directory-equality
    /// rule must hold at the destination — and that index is generated
    /// even for a vendor that would otherwise copy verbatim. A plain
    /// skill under its own name — or a `SKILL.md` that does not parse —
    /// installs byte-identical to the canonical tree, preserving the
    /// historical verbatim contract.
    fn materialize_skill(
        self,
        name: &str,
        artifact_root: &Path,
        dest: &Path,
    ) -> Result<Vec<MaterializedFile>, InstallError> {
        let mut out = Vec::new();
        copy_tree(artifact_root, dest, &mut out)?;

        let index_src = artifact_root.join("SKILL.md");
        if index_src.is_file() {
            let doc_bytes = std::fs::read(&index_src).map_err(|e| target_io(&index_src, e))?;
            let doc = String::from_utf8_lossy(&doc_bytes);
            // Rebind BEFORE the vendor render so every vendor projects
            // from the corrected document.
            let rebound = super::render::rebind_skill_name(&doc, name);
            let effective = rebound.as_deref().unwrap_or(&doc);
            let rendered = self
                .vendor()
                .skill_index(effective)
                .map_err(|e| materialize_failed(e.to_string()))?;
            let generated_doc = match rendered {
                Some(rendered) => {
                    for warning in &rendered.warnings {
                        tracing::warn!("{warning}");
                    }
                    Some(rendered.document)
                }
                // No vendor transform — but a rebound index is still a
                // generated file, never the canonical bytes.
                None => rebound,
            };
            if let Some(text) = generated_doc {
                let index_dest = dest.join("SKILL.md");
                std::fs::write(&index_dest, text.as_bytes()).map_err(|e| target_io(&index_dest, e))?;
                for file in &mut out {
                    if file.path == index_dest {
                        file.generated = true;
                    }
                }
            }
        }

        out.sort_by(|a, b| a.path.cmp(&b.path));
        Ok(out)
    }

    /// Materialize a rule: the index goes through the vendor's
    /// [`Vendor::rule_index`] transform (`None` ⇒ verbatim copy); an
    /// optional support directory is copied verbatim beside the index for
    /// every client.
    fn materialize_rule(
        self,
        name: &str,
        artifact_root: &Path,
        dest: &Path,
        scope: ConfigScope,
        pinned: &str,
        support_dir: Option<&Path>,
    ) -> Result<Vec<MaterializedFile>, InstallError> {
        let bytes = std::fs::read(artifact_root).map_err(|e| target_io(artifact_root, e))?;
        if let Some(parent) = dest.parent() {
            std::fs::create_dir_all(parent).map_err(|e| target_io(parent, e))?;
        }

        let doc = String::from_utf8_lossy(&bytes);
        let parsed = RuleFrontmatter::parse_doc(&doc, artifact_root).map_err(|e| materialize_failed(e.to_string()))?;
        let rendered = self
            .vendor()
            .rule_index(&parsed, scope, pinned)
            .map_err(|e| materialize_failed(e.to_string()))?;

        let mut out = match rendered {
            // Canonical bytes are what this vendor reads — verbatim copy.
            None => {
                std::fs::write(dest, &bytes).map_err(|e| target_io(dest, e))?;
                vec![MaterializedFile {
                    path: dest.to_path_buf(),
                    generated: false,
                }]
            }
            Some(rendered) => {
                for warning in &rendered.warnings {
                    tracing::warn!("{warning}");
                }
                std::fs::write(dest, rendered.document.as_bytes()).map_err(|e| target_io(dest, e))?;
                vec![MaterializedFile {
                    path: dest.to_path_buf(),
                    generated: true,
                }]
            }
        };

        // Copy the sibling support tree verbatim (all clients, no transform)
        // beside the index at `<dest_parent>/<name>/`, so the index's
        // relative links resolve.
        if let Some(src) = support_dir
            && let Some(parent) = dest.parent()
        {
            copy_tree(src, &parent.join(name), &mut out)?;
        }

        out.sort_by(|a, b| a.path.cmp(&b.path));
        Ok(out)
    }

    /// Materialize an agent: the single file goes through the vendor's
    /// [`Vendor::agent_index`] transform (`None` ⇒ verbatim copy). Agents
    /// have no support directory. An unparseable agent document is a
    /// materialize failure — unlike a rule, the frontmatter is required.
    fn materialize_agent(
        self,
        artifact_root: &Path,
        dest: &Path,
        pinned: &str,
    ) -> Result<Vec<MaterializedFile>, InstallError> {
        let bytes = std::fs::read(artifact_root).map_err(|e| target_io(artifact_root, e))?;
        if let Some(parent) = dest.parent() {
            std::fs::create_dir_all(parent).map_err(|e| target_io(parent, e))?;
        }

        let doc = String::from_utf8_lossy(&bytes);
        let parsed = AgentFrontmatter::parse_doc(&doc, artifact_root).map_err(|e| materialize_failed(e.to_string()))?;
        let rendered = self
            .vendor()
            .agent_index(&parsed, pinned)
            .map_err(|e| materialize_failed(e.to_string()))?;

        let out = match rendered {
            // Canonical bytes are what this vendor reads — verbatim copy.
            None => {
                std::fs::write(dest, &bytes).map_err(|e| target_io(dest, e))?;
                vec![MaterializedFile {
                    path: dest.to_path_buf(),
                    generated: false,
                }]
            }
            Some(rendered) => {
                for warning in &rendered.warnings {
                    tracing::warn!("{warning}");
                }
                std::fs::write(dest, rendered.document.as_bytes()).map_err(|e| target_io(dest, e))?;
                vec![MaterializedFile {
                    path: dest.to_path_buf(),
                    generated: true,
                }]
            }
        };
        Ok(out)
    }
}

fn materialize_failed(msg: String) -> InstallError {
    InstallError::without_reference(InstallErrorKind::MaterializeFailed(msg))
}

/// Recursively copy every regular file under `src` into `dst`, preserving
/// the relative structure. Records each written file as a canonical
/// (non-generated) [`MaterializedFile`].
fn copy_tree(src: &Path, dst: &Path, out: &mut Vec<MaterializedFile>) -> Result<(), InstallError> {
    let meta = std::fs::symlink_metadata(src).map_err(|e| target_io(src, e))?;
    if meta.is_dir() {
        std::fs::create_dir_all(dst).map_err(|e| target_io(dst, e))?;
        let mut children: Vec<PathBuf> = std::fs::read_dir(src)
            .map_err(|e| target_io(src, e))?
            .map(|e| e.map(|e| e.path()))
            .collect::<std::io::Result<Vec<_>>>()
            .map_err(|e| target_io(src, e))?;
        children.sort();
        for child in children {
            let name = child.file_name().unwrap_or_default();
            copy_tree(&child, &dst.join(name), out)?;
        }
    } else if meta.is_file() {
        if let Some(parent) = dst.parent() {
            std::fs::create_dir_all(parent).map_err(|e| target_io(parent, e))?;
        }
        std::fs::copy(src, dst).map_err(|e| target_io(dst, e))?;
        out.push(MaterializedFile {
            path: dst.to_path_buf(),
            generated: false,
        });
    }
    Ok(())
}

fn target_io(path: &Path, source: std::io::Error) -> InstallError {
    InstallError::without_reference(InstallErrorKind::TargetIo {
        path: path.to_path_buf(),
        source,
    })
}

